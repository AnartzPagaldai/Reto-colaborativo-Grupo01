    CREATE OR REPLACE PROCEDURE INFORME_PARTIDOS_POR_JORNADA (C_PARTIDOS OUT SYS_REFCURSOR) AS
        BEGIN
        
            OPEN C_PARTIDOS FOR 
            SELECT
                S.TIPO TIPO_SPLIT,
                J.NUM_JORNADA NUMERO_JORNADA,
                J.TIPO TIPO_JORNADA,
                P.*,
                E1.NOMBRE EQUIPO1,
                E2.NOMBRE EQUIPO2
            FROM SPLITS S, JORNADAS J, PARTIDOS P, EQUIPOS E1, EQUIPOS E2
            WHERE S.ID=J.ID_SPLIT AND J.ID=P.ID_JORNADA 
            AND ID_EQUIPO1 = E1.ID AND ID_EQUIPO2 = E2.ID 
            AND S.ID=(SELECT MAX(ID) FROM SPLITS) ORDER BY J.ID, P.ID;
    END INFORME_PARTIDOS_POR_JORNADA;
    
